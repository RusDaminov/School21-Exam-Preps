#include <unistd.h>

void put(char c)
{
    write(1, &c, 1);
}

int main()
{
    int i = 0;
    int ac = 1;
    char av[11] = "world hello";
    
    if (ac < 1)
        write(1, "\n", 1);
    else
        while(av[i] != '\0')
        {
            put(av[i]);
            i++;
        }
    write(1, "\n", 1);
    return (0);
}
